                <p>Produced by Yunyun Wang.</p>

                <p>Google has a racism problem. And nearly three years after an incident where software engineer Jacky Alciné reported that Google Photos’ image recognition algorithms were misclassifying black people as “gorillas”, Google has since blocked the gorilla identifier altogether rather than risking another incidence of mis-categorization. However, there may be more than meets the eye, because a recent research study conducted by Joy Buolamwini of MIT Media labs identified several companies with facial recognition softwares that consistently return less accurate results for female faces compared to males, and even lower accuracy for faces of women of color.</p>

